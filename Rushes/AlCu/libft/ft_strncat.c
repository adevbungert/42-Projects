#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int			i;
	size_t		j;

	j = 0;
	i = 0;
	while (s1[i])
		i++;
	while (*s2 && j < n)
	{
		s1[i] = *s2;
		i++;
		s2++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
