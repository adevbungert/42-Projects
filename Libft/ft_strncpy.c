char	*ft_strncpy(char *dst, char *src, int n)
{
	int i;

	i = 0;
	while (src[i] != '\0' || i <= n)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\n';
	return (dest);
}
